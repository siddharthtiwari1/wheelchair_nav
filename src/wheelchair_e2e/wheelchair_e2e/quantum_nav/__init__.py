#   quantum_trajectory_planner.py — original planner (uniform superposition)
#   quantum_planner_v2.py         — modified formulations (cost-encoded, phase, temporal)
#   baselines.py                  — 7 classical baselines for ablation
#   metrics.py                    — evaluation metrics (Spearman, calibration, etc.)
#   visualize_quantum_nav.py      — synthetic visualization figures
